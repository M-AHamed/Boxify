                <span class="Text2">Start by classifying your inventory to ensure you’re organizing even your slowest moving items accordingly. 
                    Profiling your stock keeping units or SKUs should be supported by data. Take inventory of what is currently 
                    in your warehouse (size, shape, quantity) and how often it moves or frequency of picking. This will help you 
                    organize your inventory into categories: A, B, C and D also known as your fast (A), medium (B), slow (C) and 
                    very slow (D) movers. Once this is charted (see below), cross reference your documented SKU velocity with the 
                    amount of time it takes to pick those items, this will tell you how much it costs to pick your inventory.</br>
                    </br>
                    If you apply the Pareto Principle (80/20 rule) here, you could imagine it makes the most sense to focus on the 
                    20% of your inventory being picked 80% of the time. If that’s the case, you’d be missing the improvement opportunity 
                    from the remaining 80% of your inventory which sits stagnant more often than not.
                </span>
                    
